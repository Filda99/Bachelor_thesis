//!*************************************************************************************************
//! void delay_ms(uint32_t ms)
//!
//! @description
//! Function
//!
//! @param    None
//!
//! @return   None
//!*************************************************************************************************
void delay_ms(uint32_t ms)
{
    uint32_t i;
    for (i = 0; i < (SystemCoreClock / 14000 * ms); i++)
    {
        __asm("NOP");
    }
}

//!*************************************************************************************************
//! void delay_us(int us)
//!
//! @description
//! Function will probably not be used.
//!
//!
//! @param    None
//!
//! @return   None
//!*************************************************************************************************
void delay_us(int us)
{
    for(int i=0; i<us*5; i++) {
    	__asm("NOP");
    }
}
