//!*************************************************************************************************
//! void mapping(void)
//!
//! @description
//! Brief description
//!
//! @param    None
//!
//! @return   None
//!*************************************************************************************************
void mapping()
{
	// distance = 2*pi*r
	// r = ~1.7
	// One rotation is ~10.68cm
	static float newDistance = 0;
	static uint32_t prevNoWheelRot = 0;

	// Get distance from previous position
	newDistance = WheelRotations - prevNoWheelRot;
	newDistance *= 10.68;

}


